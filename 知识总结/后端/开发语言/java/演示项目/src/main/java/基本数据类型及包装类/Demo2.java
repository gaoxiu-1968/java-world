package 基本数据类型及包装类;
//包装类
public class Demo2 {
    private Byte a;
    private Short b;
    private Integer c;
    private Long d;
    private Float f;
    private Double g;
    private Boolean h;
    private Character i;

    public Byte getA() {
        return a;
    }

    public void setA(Byte a) {
        this.a = a;
    }

    public Short getB() {
        return b;
    }

    public void setB(Short b) {
        this.b = b;
    }

    public Integer getC() {
        return c;
    }

    public void setC(Integer c) {
        this.c = c;
    }

    public Long getD() {
        return d;
    }

    public void setD(Long d) {
        this.d = d;
    }

    public Float getF() {
        return f;
    }

    public void setF(Float f) {
        this.f = f;
    }

    public Double getG() {
        return g;
    }

    public void setG(Double g) {
        this.g = g;
    }

    public Boolean getH() {
        return h;
    }

    public void setH(Boolean h) {
        this.h = h;
    }

    public Character getI() {
        return i;
    }

    public void setI(Character i) {
        this.i = i;
    }
}
