package 基本数据类型及包装类;
//基本类型
public class Demo1 {
    private byte a;
    private short b;
    private int c;
    private long d;
    private float f;
    private double g;
    private boolean h;
    private char i;

    public byte getA() {
        return a;
    }

    public void setA(byte a) {
        this.a = a;
    }

    public short getB() {
        return b;
    }

    public void setB(short b) {
        this.b = b;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }

    public long getD() {
        return d;
    }

    public void setD(long d) {
        this.d = d;
    }

    public float getF() {
        return f;
    }

    public void setF(float f) {
        this.f = f;
    }

    public double getG() {
        return g;
    }

    public void setG(double g) {
        this.g = g;
    }

    public boolean isH() {
        return h;
    }

    public void setH(boolean h) {
        this.h = h;
    }

    public char getI() {
        return i;
    }

    public void setI(char i) {
        this.i = i;
    }
}
